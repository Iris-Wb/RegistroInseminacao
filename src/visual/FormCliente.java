    public void atualizaTabela(){
        listCliente.clear();
        listCliente.addAll(daoCliente.getLista());
        int linha = listCliente.size()-1;
        if (linha>=0){
            tabelaCliente.setRowSelectionInterval(linha, linha);
            tabelaCliente.scrollRectToVisible(tabelaCliente.getCellRect(linha, linha, true));
        }
    }
    
    private void trataEdicao(boolean editando){
        botaoCancelar.setEnabled(editando);
        botaoSalvar.setEnabled(editando);
        botaoEditar.setEnabled(!editando);
        int linha = listCliente.size() -1;
        if(linha<0){
            botaoExcluir.setEnabled(false);
            textoNome.setText("");
            textoCodigo.setText("");
            textoCpf.setText("");
            textoNascimento.setText("");
            textoTelefone.setText("");
        }
        else{
            botaoExcluir.setEnabled(!editando);
        }
        botaoNovo.setEnabled(!editando);
        botaoAnterior.setEnabled(!editando);
        botaoFechar.setEnabled(!editando);
        botaoPrimeiro.setEnabled(!editando);
        botaoProximo.setEnabled(!editando);
        botaoUltimo.setEnabled(!editando); 
        textoCodigo.setEnabled(editando);
        textoCpf.setEnabled(editando);
        textoTelefone.setEnabled(editando);
        textoNome.setEnabled(editando);
        textoNascimento.setEnabled(editando);
        cbxLocal.setEnabled(editando);
        tabelaCliente.setEnabled(editando);
    }

    public boolean validaCampos(){
        if (!(textoNome.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme o nome do cliente.");
            textoNome.requestFocus();
            return false;
        }
        if (!(textoCpf.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme o nome CPF do cliente.");
            textoCpf.requestFocus();
            return false;
        }
        if (!(textoNascimento.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme a data de nascimento.");
            textoNascimento.requestFocus();
            return false;
        }
        
        
        if (textoNascimento.getText().length()>0){
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            try{
                sdf.parse(textoNascimento.getText());
            }catch (Exception erro){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme uma data válida.");
            textoNascimento.requestFocus();
            return false;
            }
        }
        
        if (!(textoTelefone.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme o telefone do cliente.");
            textoTelefone.requestFocus();
            return false;
        }
        if (!(cbxLocal.getSelectedIndex()>=0)){
            JOptionPane.showMessageDialog(null, "ATENÇÃO\nInforme o local correspondente.");
            cbxLocal.requestFocus();
            return false;
        }
        
        return true;
    }

    
    
