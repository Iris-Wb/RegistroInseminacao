        // TODO add your handling code here:
        int opcao = JOptionPane.showOptionDialog(null, "Deseja excluir esse registro?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new String[]{"Sim", "Não"}, "Sim");
